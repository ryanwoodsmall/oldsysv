"uname" prints the name of the UNIX system you are using.  Besides
determining the system name, you can retrieve other information
such as nodename, system release and version, and machine hardware
name.

00This option prints the nodename.  The nodename may be a name
that the system is known by to a communications network. [-n]
01This option prints the operating system release. [-r]
02This option prints the operating system version. [-v]
03This option prints the hardware name of the computer
that you are using. [-m]
04This option prints the system name, nodename, operating
system release and version, and machine hardware name.
It makes no sense to use this option with any of the other
options since the information provided is redundant. [-a]
05ASSIST appends anything you type here to the command line.
You can use this field for "piping," "redirection," or to
execute a command in the "background."  However, since
ASSIST does not perform any error checking on this field,
you should use it with caution.
