"cp" copies the input file(s) to the target output file.  Under no 
circumstances can the input file and the target file be the same.  
If the target is a directory, then one or more files are copied
to that directory.  If the target is a file, its contents are destroyed.
If target is not an existing file, a new file is created.  The input
file cannot be a directory.

00Type the names of files that you want to copy.  You may specify
more than one file name.
01Type the name of a file or directory you would like "cp" to copy 
the files to.  If you specify more than one input file, then
you must type a directory name as the output.  If you type the
name of an existing file as the output file, then its contents
are destroyed.  If you type the name of a directory, the files
being copied are added to the directory.
