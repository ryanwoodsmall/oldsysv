The "id" command requires no input.  It returns your user id
and group id.  These ids are shown in both their mnemonic
and numeric forms.  Each UNIX system user is known by a
user id.  This is often the name that you enter in response
to the "login:" prompt.  Each user also is associated with
a group of users.  Many users are in the "other" group, but
your administrator may define different groups on your system.
The user id and group id that you might be familiar with are
usually alphabetic strings.  This is the mnemonic form of your
id.  Internally, the UNIX system stores this as a numeric form.
The "id" command shows you both forms.
